// Modules
// api
import api from '../../utils/api';


// Ext Action Creators
import { setAlert } from "./alert";

// Ext action types
import { ACCOUNT_DELETED } from "./auth";

// Action types
export const GET_PROFILE = "PLUTO/PROFILES/GET_PROFILE";
export const PROFILE_ERROR = "PLUTO/PROFILES/PROFILE_ERROR";
export const CLEAR_PROFILE = "PLUTO/PROFILES/CLEAR_PROFILE";
export const UPDATE_PROFILE = "PLUTO/PROFILES/UPDATE_PROFILE";
export const GET_PROFILES = 'PLUTO/PROFILES/GET_PROFILES';
export const GET_REPOS = 'PLUTO/PROFILES/GET_REPOS';
export const NO_REPOS = 'PLUTO/PROFILES/NO_REPOS';


// Reducer

const initialState = {
  profile: null,
  profiles: [],
  repos: [],
  loading: true,
  error: {},
};

export default (state = initialState, action) => {
  const { type, payload } = action;

  switch (type) {
    case GET_PROFILE:
    case UPDATE_PROFILE:
      return {
        ...state,
        profile: payload,
        loading: false,
      };
    case GET_PROFILES:
      return {
        ...state,
        profiles: payload,
        loading: false,
      };
    case PROFILE_ERROR:
      return {
        ...state,
        error: payload,
        loading: false,
        profile: null,
      };
    case CLEAR_PROFILE:
      return {
        ...state,
        profile: null,
        repos: [],
      };
    case GET_REPOS:
      return {
        ...state,
        repos: payload,
        loading: false,
      };
    case NO_REPOS:
      return {
        ...state,
        repos: [],
      };
    default:
      return state;
  }
}

// Action Creators

// Get current users profile
export const getCurrentProfile = () => async (dispatch) => {
  try {
    const res = await api.get("/profiles/me");

    dispatch({
      type: GET_PROFILE,
      payload: res.data,
    });
    
  } catch (err) {
    dispatch({
      type: PROFILE_ERROR,
      payload: { msg: err.response.statusText, status: err.response.status },
    });
  }
};

